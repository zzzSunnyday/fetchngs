#!/usr/bin/env python

"""
Download files from PRIDE Archive using FTP links from the file listing TSV.

Reads the files TSV generated by pride_metadata.py, filters by category,
and downloads each file via FTP/HTTP using urllib.
"""

import argparse
import csv
import hashlib
import logging
import sys
import time
from pathlib import Path
from urllib.error import URLError
from urllib.request import urlopen

logger = logging.getLogger()


def parse_args(args=None):
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(
        description="Download PRIDE project files from FTP links.",
    )
    parser.add_argument("files_tsv", type=Path, help="Input file listing TSV from pride_metadata.py.")
    parser.add_argument("output_dir", type=Path, help="Output directory for downloaded files.")
    parser.add_argument(
        "--category",
        type=str,
        default=None,
        help="File category to download (e.g. RAW, PEAK, RESULT, OTHER). Downloads all if not specified.",
    )
    parser.add_argument(
        "--checksum",
        action="store_true",
        default=False,
        help="Verify file checksums after download.",
    )
    parser.add_argument(
        "-l",
        "--log-level",
        default="WARNING",
        choices=("CRITICAL", "ERROR", "WARNING", "INFO", "DEBUG"),
    )
    return parser.parse_args(args)


def download_file(url, output_path, max_retries=3, initial_delay=5):
    """Download a file from URL with retry logic."""
    delay = initial_delay

    for attempt in range(max_retries + 1):
        try:
            with urlopen(url, timeout=300) as response:
                with open(output_path, "wb") as f:
                    while True:
                        chunk = response.read(8192)
                        if not chunk:
                            break
                        f.write(chunk)
            return True
        except URLError as e:
            if attempt < max_retries:
                logger.warning(f"Download failed for {url}: {e.reason}. Retrying in {delay}s...")
                time.sleep(delay)
                delay *= 2
            else:
                logger.error(f"Failed to download {url} after {max_retries} retries: {e.reason}")
                return False
        except Exception as e:
            if attempt < max_retries:
                logger.warning(f"Error downloading {url}: {e}. Retrying in {delay}s...")
                time.sleep(delay)
                delay *= 2
            else:
                logger.error(f"Failed to download {url} after {max_retries} retries: {e}")
                return False

    return False


def verify_checksum(filepath, expected_checksum, checksum_type="MD5"):
    """Verify file checksum."""
    if not expected_checksum:
        return True

    hash_func = hashlib.md5() if checksum_type.upper() == "MD5" else hashlib.sha256()
    with open(filepath, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            hash_func.update(chunk)

    actual = hash_func.hexdigest()
    if actual != expected_checksum.lower():
        logger.error(f"Checksum mismatch for {filepath}: expected {expected_checksum}, got {actual}")
        return False
    return True


def main(args=None):
    """Main entry point."""
    args = parse_args(args)
    logging.basicConfig(level=args.log_level, format="[%(levelname)s] %(message)s")

    if not args.files_tsv.exists():
        logger.error(f"File listing TSV not found: {args.files_tsv}")
        sys.exit(1)

    args.output_dir.mkdir(parents=True, exist_ok=True)

    # Read file listing
    with open(args.files_tsv) as f:
        reader = csv.DictReader(f, delimiter="\t")
        files = list(reader)

    if not files:
        logger.error("No files found in file listing TSV.")
        sys.exit(1)

    # Filter by category if specified
    if args.category:
        category_upper = args.category.upper()
        files = [f for f in files if f.get("fileCategory", "").upper() == category_upper]

    if not files:
        logger.warning(f"No files matching category '{args.category}' found.")
        sys.exit(0)

    # Download each file
    downloaded = 0
    failed = 0
    for entry in files:
        filename = entry.get("fileName", "")
        ftp_link = entry.get("ftpLink", "")

        if not ftp_link:
            logger.warning(f"No FTP link for {filename}, skipping.")
            continue

        output_path = args.output_dir / filename

        if output_path.exists():
            logger.info(f"File already exists, skipping: {filename}")
            downloaded += 1
            continue

        logger.info(f"Downloading {filename} from {ftp_link}")
        if download_file(ftp_link, output_path):
            downloaded += 1

            if args.checksum:
                checksum = entry.get("checksum", "")
                checksum_type = entry.get("checksumType", "MD5")
                if checksum and not verify_checksum(output_path, checksum, checksum_type):
                    logger.error(f"Checksum verification failed for {filename}")
                    failed += 1
        else:
            failed += 1

    logger.info(f"Downloaded {downloaded} file(s), {failed} failed.")
    if failed > 0:
        sys.exit(1)


if __name__ == "__main__":
    sys.exit(main())
